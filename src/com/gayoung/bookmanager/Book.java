package com.gayoung.bookmanager;

public class Book {
    String code;
    String title;
    String author;
    String genre;
    String date;

    public Book(String code, String title, String author, String genre, String date) {
        this.code = code;
        this.title = title;
        this.author = author;
        this.genre = genre;
        this.date = date;

    }
}
